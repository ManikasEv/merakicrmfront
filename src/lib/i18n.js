export const DEFAULT_LANG = 'el'

export function localeFor(lang) {
  if (lang === 'en') return 'en-GB'
  if (lang === 'de') return 'de-CH'
  return 'el-GR'
}

export function monthShort(ym, lang) {
  const [y, m] = ym.split('-')
  const d = new Date(Number(y), Number(m) - 1, 1)
  return d.toLocaleDateString(localeFor(lang), { month: 'short', year: '2-digit' })
}

